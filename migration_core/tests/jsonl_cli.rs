#[test]
fn helper_binary_frame_chunks_expanded_utf8_plan_under_wire_cap() {
    let column_name = "열".repeat(220_000);
    let request = json!({
        "command": "plan",
        "request_id": "cli-large-frame-1",
        "payload": {
            "source_engine": "mysql",
            "target_engine": "postgresql",
            "schema": {
                "tables": [{
                    "name": "large_names",
                    "columns": [{
                        "name": column_name,
                        "type": "int(11) auto_increment",
                        "nullable": false,
                        "primary_key": true
                    }]
                }]
            }
        }
    });
    let input = format!("{request}\n");
    assert!(input.as_bytes().len() < 1_048_576);

    let output = run_helper_output(&input);
    let frames = output
        .split_inclusive(|byte| *byte == b'\n')
        .filter(|frame| !frame.is_empty())
        .collect::<Vec<_>>();

    assert!(frames.len() > 1);
    assert!(frames.iter().all(|frame| frame.len() <= 1_048_576));
    assert!(frames.iter().all(|frame| std::str::from_utf8(frame).is_ok()));
    assert!(frames.iter().any(|frame| {
        serde_json::from_slice::<Value>(frame).unwrap()["event"] == "payload_chunk"
    }));
}

#[test]
fn helper_binary_rejects_oversized_frame_and_processes_next_request() {
    let mut input = "x".repeat(1_048_576);
    input.push_str("x\n");
    input.push_str(
        "{\"command\":\"service.hello\",\"request_id\":\"after-large\",\"payload\":{}}\n",
    );

    let events = run_helper_lines(&input);

    assert_eq!(events.len(), 2);
    assert_eq!(events[0]["code"], "jsonl_frame_too_large");
    assert_eq!(events[0]["command"], Value::Null);
    assert_eq!(events[1]["request_id"], "after-large");
}

#[test]
fn helper_binary_rejects_unterminated_jsonl_frame() {
    let events = run_helper_lines(
        "{\"command\":\"service.hello\",\"request_id\":\"unterminated\",\"payload\":{}}",
    );

    assert_eq!(events.len(), 1);
    assert_eq!(events[0]["code"], "jsonl_frame_missing_newline");
    assert_eq!(events[0]["command"], Value::Null);
}

#[test]
fn helper_binary_rejects_invalid_request_ids_without_null_envelopes() {
    for input in [
        "{\"command\":\"service.hello\",\"payload\":{}}\n",
        "{\"command\":\"service.hello\",\"request_id\":null,\"payload\":{}}\n",
        "{\"command\":\"service.hello\",\"request_id\":7,\"payload\":{}}\n",
        "{\"command\":\"service.hello\",\"request_id\":\"\",\"payload\":{}}\n",
    ] {
        let events = run_helper_lines(input);
        assert_eq!(events.len(), 1);
        assert_eq!(events[0]["event"], "error");
        assert!(matches!(events[0]["request_id"].as_str(), Some(value) if !value.is_empty()));
        assert_ne!(events[0]["request_id"], Value::Null);
    }
}
